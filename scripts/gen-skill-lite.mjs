#!/usr/bin/env node
/**
 * gen-skill-lite.mjs - project the canonical portable doctrine into per-tool skill files
 * for NON-Claude tools (Cursor, Codex/AGENTS.md, Gemini, Copilot).
 *
 * Single source: references/core-doctrine.md (hand-authored at "lite altitude"). This script
 * only READS that file + VERSION and WRITES under skill-lite/ - it never touches SKILL.md,
 * hooks/, agents/, .claude-plugin/, scripts/ or templates/, so the live Claude plugin (v1.7.x)
 * cannot regress. Improving the doctrine = edit core-doctrine.md, run this, and every tool's
 * skill updates from the one source. The drift guard (scripts/test/skill-lite-sync.test.sh)
 * runs --check so the generated files can never be hand-edited out of sync.
 *
 * Deterministic + timestamp-free (stamps only the committed VERSION), so --check never flaps.
 *
 * Usage: node scripts/gen-skill-lite.mjs          write the per-tool files
 *        node scripts/gen-skill-lite.mjs --check  regenerate in-memory, exit 1 on any drift
 */
import { readFileSync, writeFileSync, mkdirSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = join(dirname(fileURLToPath(import.meta.url)), "..");
const DOCTRINE = join(ROOT, "references/core-doctrine.md");
const OUT = join(ROOT, "skill-lite");
const VERSION = readFileSync(join(ROOT, "VERSION"), "utf8").trim();
const CHECK = process.argv.includes("--check");

// Codex truncates a combined project AGENTS.md past 32KiB; Cursor/Gemini/Copilot prefer small
// always-load files. The doctrine asymptotes near 17KB once the verbatim closed lists (banned
// faces, the 12 imagery motifs, the perceptual-floor numbers) are kept faithfully, which are
// the anti-slop differentiator, so the ceiling sits at 20KB (still well under Codex's 32KiB).
const BUDGET = 20480;

const body = readFileSync(DOCTRINE, "utf8").trimEnd();
const bytes = Buffer.byteLength(body, "utf8");
if (bytes > BUDGET) {
  console.error(`references/core-doctrine.md is ${bytes} bytes, over the ${BUDGET}-byte always-load budget. Tighten it before regenerating.`);
  process.exit(1);
}

const banner =
  `<!-- AUTO-GENERATED from references/core-doctrine.md by scripts/gen-skill-lite.mjs (Palate ${VERSION}). ` +
  `DO NOT EDIT THIS FILE: edit references/core-doctrine.md and run \`node scripts/gen-skill-lite.mjs\`. -->\n\n`;

// Per-tool wrappers. AGENTS.md is the universal base (the agents.md open standard, read by
// Codex natively and Cursor/Copilot/Gemini secondarily). Cursor needs a .mdc with frontmatter
// (a .md in .cursor/rules is silently ignored). Copilot + Gemini take the body verbatim.
const targets = [
  { out: "AGENTS.md", wrap: (b) => banner + b + "\n" },
  { out: "cursor/palate.mdc", wrap: (b) => `---\nalwaysApply: true\n---\n` + banner + b + "\n" },
  { out: "gemini/GEMINI.md", wrap: (b) => banner + b + "\n" },
  { out: "copilot/copilot-instructions.md", wrap: (b) => banner + b + "\n" },
];

let drift = false;
for (const t of targets) {
  const content = t.wrap(body);
  const path = join(OUT, t.out);
  if (CHECK) {
    const cur = existsSync(path) ? readFileSync(path, "utf8") : "";
    if (cur !== content) {
      console.error(`DRIFT: skill-lite/${t.out} differs from a fresh projection of core-doctrine.md.`);
      drift = true;
    }
  } else {
    mkdirSync(dirname(path), { recursive: true });
    writeFileSync(path, content);
  }
}

if (CHECK) {
  if (drift) { console.error("Re-sync with: node scripts/gen-skill-lite.mjs"); process.exit(1); }
  console.log("skill-lite is in sync with references/core-doctrine.md.");
} else {
  console.log(`generated ${targets.length} per-tool skill-lite files in skill-lite/ (Palate ${VERSION}, doctrine ${bytes}B).`);
}
